n = int(input())

for h_24 in range(24): # 0 ~ 23
    h = h_24
    h %= 12 # h = (h % 12)
    for m in range(60): # 0 ~ 59
        angle = abs((30 * h) - (5.5 * m))
        if angle == n:
            print(f'{h_24}:{m}')



# 時針每小時走 30 度(360 / 12)，每分鐘走 0.5 度(30 / 60)
# 分針每小時走 360 度(360 / 1)，每分鐘走 6 度(360 / 60)
# 因此每分鐘時針會增加 0.5 度
# 當 h 點 m 分時角度為 [(30h + 0.5m) - 6m] = |30h - 5.5m|
# 沒有絕對值時，當 6m > (30h + 0.5m) 則角度為負的